<p>Ustawa o dostępie do informacji publicznej podaje kategorie informacji, które organizacja jest zobowiązana udostępniać w BIP, opisane poniżej.</p>

<p>Ustawa nie określa w jaki sposób te informacje należy udostępniać. To znaczy, że każda organizacja może samodzielnie określić strukturę swojego BIP-u, układ stron i ich zawartość. Korzystając z wtyczki BIP Pages możesz utworzyć potrzebne strony i zamieścić na nich odpowiednie odnośniki, aby pozwolić obywatelom na łatwe poruszanie się po BIP organizacji – tak jak w przypadku tworzenia treści na każdej innej strony internetowej.</p>

