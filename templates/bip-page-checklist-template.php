<section id="organizacja">
<h2>Informacje o organizacji</h2>

<p>BIP musi zawierać podstawowe informacje o organizacji. Należy opisać co najmniej:</p>

<ol>
<li>formę prawną organizacji,
<li>organizację wewnętrzną, tj. podział na oddziały terenowe, itp.
<li>przedmiot działalności (cel lub misję statutową),
<li>organy i osoby sprawujących w nich funkcje i ich kompetencje.
</ol>
</section>

<section id="majatek">
<h2>Informacje o majątku</h2>

<p>Ustawa wymaga zamieszczania w BIP informacji o funduszach uzyskanych od Skarbu Państwa oraz samorządu terytorialnego.

<p>Informacje te mogą mieć postać rocznych sprawozdań finansowych, a w przypadku organizacji pożytku publicznego – dodatkowo kopii sprawozdań z działalności pożytku publicznego dla Narodowego Instytutu Wolności.
</section>

<section id="zadania-publiczne">
<h2>Informacje o realizowanych zadaniach publicznych</h2>

<p>Jeśli organizacja realizuje zadania publiczne – zlecone lub dofinansowywane ze środków rządowych lub samorządowych – dla każdego z tych zadań w BIP musi znaleźć się informacja o:</p>
<ul>
  <li>sposobie jego realizacji,
  <li>wykonaniu zadania,
  <li>skutkach jego realizacji (efektach).
</ul>

<p>Wymóg ten może być realizowany poprzez zamieszczenie w BIP sprawozdania końcowego lub innego rozliczenia realizowanego zadania.
</section>

<section id="kontrole">
<h2>Informacje o kontrolach</h2>

<p>Ustawa wymaga, aby w BIP udostępniana była: "dokumentacja przebiegu i efektów kontroli oraz wystąpienia, stanowiska, wnioski i opinie podmiotów ją przeprowadzających" (art. 6. pkt. 4 lit. a tiret drugie).

<p>Sugerujemy w tym celu stworzyć stronę BIP "Kontrole" lub kategorię "Kontrole".
</section>

<section id="rejestry">
<h2>Informacje o prowadzonych rejestrach</h2>

<p>W BIP musi się znaleźć informacja o prowadzonych przez organizację rejestrach, ewidencjach i archiwach.

<p>W przypadku większości organizacji lista ta ograniczać się będzie do rejestrów związanych z przepisami o ochronie danych osobowych (RODO).

<p>Ustawa wymaga również podania zasad udostępniania danych zwartych w rejestrach i ewidencjach, przy czym zasady te mogą wskazywać, że dane nie są udostępniane publicznie.
</section>

<section id="dodatkowe">
<h2>Opcjonalne i dodatkowe informacje w BIP</h2>

<p>Organizacja może zamieszczać w BIP dodatkowe informacje, których publikacja w BIP nie jest wymagana przez ustawę.

<p>Publikacja dodatkowych informacji w BIP zwiększa transparentność organizacji.

<p>Przykładami informacji opcjonalnych są:</p>
<ul>
  <li>statut lub regulamin organizacji,
  <li>rejestr umów.
</ul>
</section>

<section id="dostepnosc">
<h2>Dostępność cyfrowa informacji dla osób z niepełnosprawnościami</h2>

<p>Zalecamy zapewnienie możliwie najwyższej dostępności zamieszczanych w BIP treści dla osób z niepełnosprawnościami i ograniczeniami.

<p>W tym celu zalecamy zamieszczanie informacji w formie tekstowej, bezpośrednio na stronach BIP.

<p>W przypadku publikacji w BIP dokumentów lub załączników w formie plików do pobrania, zalecamy ich publikację w formacie DOCX lub PDF. Pliki powinny powstawać na bazie cyfrowych tekstów dokumentów. Nie powinny być skanami dokumentów, ze względu na brak możliwości odczytania takich plików za pomocą czytników ekranowych.

<p>Zapewnienie dostępności cyfrowej jest obowiązkowe dla organizacji prowadzących działalność w sferze:</p>
<ul>
  <li>ochrony i promocji zdrowia,
  <li>działalności na rzecz osób niepełnosprawnych,
  <li>działalności na rzecz osób w wieku emerytalnym.
</ul>

<p>Powyższy obowiązek wprowadza <a href="https://isap.sejm.gov.pl/isap.nsf/DocDetails.xsp?id=WDU20190000848">ustawa o dostępności cyfrowej stron internetowych i aplikacji mobilnych podmiotów publicznych</a>.</p>
</section>

<section id="podstawa-prawna">
<h2>Podstawa prawna</h2>

<p>Obowiązek prowadzenia Biuletynu Informacji Publicznej wprowadza <a href="http://isap.sejm.gov.pl/isap.nsf/DocDetails.xsp?id=WDU20011121198">ustawa o dostępie do informacji publicznej</a>.</p>

<p>Do organizacji społecznych odnosi się art. 4. ust. 1. pkt. 5.:

<blockquote>
<strong>Art. 4.</strong>
<p>1. Obowiązane do udostępniania informacji publicznej są władze publiczne oraz inne podmioty wykonujące zadania publiczne [...]:</p>
<p>5) podmioty reprezentujące inne osoby lub jednostki organizacyjne, które wykonują zadania publiczne lub dysponują majątkiem publicznym,</p>
</blockquote>

<p>Obowiązkową zawartość BIP określa art. 8. ust. 3. ustawy, który odsyła do wybranych części art. 6.
</section>

